def 添加问候语(原函数):
    def 包装函数(名字):
        print("您好！")          # 在原函数执行前添加动作
        原函数(名字)            # 调用原函数
        print("再见！\n")        # 在原函数执行后添加动作
    return 包装函数             # 返回包装后的函数

@添加问候语
def 打招呼(名字):
    print(f"{名字}，今天过得怎么样？")

