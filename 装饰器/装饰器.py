import time

def 记录耗时(原函数):
    def 包装函数(*参数, **关键字参数):
        开始时间 = time.time()
        结果 = 原函数(*参数, **关键字参数)  # 执行原函数
        耗时 = time.time() - 开始时间
        print(f"{原函数.__name__} 执行耗时: {耗时:.2f}秒,结果为{结果}")
        return 结果
    return 包装函数

@记录耗时
def 计算累加和(最大值):
    return sum(range(最大值+1))

计算累加和(1000000)  # 输出：计算累加和 执行耗时: 0.02秒
